class Admin extends Authenticatable
{
    use HasFactory, HasRoles, HasImage;

    protected $fillable = [
        'name', 'email', 'phone', 'password', 'image', 'is_active', 'address', 'salary'
    ];

    protected $hidden = ['password'];


    public function scopeActive($query)
    {
        return $query->whereIsActive(1);
    }


    public function payrolls()
    {
        return $this->hasMany(Payroll::class);
    }

    public function shifts()
    {
        return $this->hasMany(Shift::class);
    }

    public function hasOpenShift()
    {
        return $this->shifts()->whereIsOpen(1)->exists();
    }


    protected static function booted()
    {
        static::deleting(function ($model) {
            if ($model->image) {
                $image = str_replace(url('/') . '/storage/', '', $model->image);
                deleteImage('uploads', $image);
            }
        });
    }


}
